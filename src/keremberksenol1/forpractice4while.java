package keremberksenol1;

public class forpractice4while {

	public static void main(String[] args) {

		int i = 1;

		while (i <= 10) {
			if (i % 2 == 1) {
				System.out.println(i);
				System.out.println("Kerem");
			} else {
				System.out.println(i);
				System.out.println("Berk");
			}
			i++;
		}

	}

}
